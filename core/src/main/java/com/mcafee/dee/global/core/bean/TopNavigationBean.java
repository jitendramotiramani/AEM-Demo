package com.mcafee.dee.global.core.bean;


public class TopNavigationBean {
	
	private String linkname;
	private String linkpath;
	private String opennewwindow;
	private String pathfield;
	public String getLinkname() {
		return linkname;
	}
	public void setLinkname(String linkname) {
		this.linkname = linkname;
	}
	public String getLinkpath() {
		return linkpath;
	}
	public void setLinkpath(String linkpath) {
		this.linkpath = linkpath;
	}
	public String getOpennewwindow() {
		return opennewwindow;
	}
	public void setOpennewwindow(String opennewwindow) {
		this.opennewwindow = opennewwindow;
	}
	public String getPathfield() {
		return pathfield;
	}
	public void setPathfield(String pathfield) {
		this.pathfield = pathfield;
	}
	
	

}
